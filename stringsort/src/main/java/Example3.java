class Example3 {
    public static void main (String[] args) {
        int v;
        double x;

        v = 10;
        x = 10;

        System.out.println("Original value of v: " + v);
        System.out.println("Original value of x: " + x);

        // Divide values by four
        v = v / 4;
        x = x / 4;

        System.out.println();
        System.out.println("Value after division: " + v);
        System.out.println("Value after division: " + x);
    }
}
